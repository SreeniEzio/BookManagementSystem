    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        DefaultTableModel model = (DefaultTableModel) tblBooks.getModel();
        model.setRowCount(0);
        String author = txtTitle.getText().trim();
        if(author.isEmpty()){
            JOptionPane.showMessageDialog(ViewBookByAuthor.this, "Enter the book name");
            txtTitle.grabFocus();
            return;
        }
        ArrayList<Object[]> books = new BookBackend().retrieveByAuthor(author);
        if(books.isEmpty()){
            JOptionPane.showMessageDialog(ViewBookByAuthor.this, "The entered book is not available");
            txtTitle.setText("");
            txtTitle.grabFocus();
            return;
        }

        for(Object[] b: books){
            String status;
            if((int)b[4] == 0){
                status = "Available";
            }else{
                status = "Not Available";
            }
            model.insertRow(tblBooks.getRowCount(), new Object[]{
                b[0], b[1], b[2], b[3], status
            });
        }
    }//GEN-LAST:event_btnSearchActionPerformed

